package com.saratrak.sisschool.admin;

public class Bus {
	private String vehiclename,uniqueId,entry1,exit1,entry2,exit2,colour;
	
	public Bus()
	{
		
	}

	public String getVehiclename() {
		return vehiclename;
	}

	public void setVehiclename(String vehiclename) {
		this.vehiclename = vehiclename;
	}

	public String getUniqueId() {
		return uniqueId;
	}

	public void setUniqueId(String uniqueId) {
		this.uniqueId = uniqueId;
	}

	public String getEntry1() {
		return entry1;
	}

	public void setEntry1(String entry1) {
		this.entry1 = entry1;
	}

	public String getExit1() {
		return exit1;
	}

	public void setExit1(String exit1) {
		this.exit1 = exit1;
	}

	public String getEntry2() {
		return entry2;
	}

	public void setEntry2(String entry2) {
		this.entry2 = entry2;
	}

	public String getExit2() {
		return exit2;
	}

	public void setExit2(String exit2) {
		this.exit2 = exit2;
	}

	public String getColour() {
		return colour;
	}

	public void setColour(String colour) {
		this.colour = colour;
	}
}
